    <div class="container">
      <p>Agradecemos por sua disposição em ajudar a salvar vidas! O processo de agendamento de doação de sangue é simples, e nosso objetivo é tornar a experiência o mais fácil e confortável possível para você. A seguir, você encontrará todas as informações necessárias para realizar seu agendamento com sucesso.</p>

      <hr>
      <h2>Por que agendar a doação de sangue?</h2>
      <p>A doação de sangue é um ato de solidariedade que pode salvar vidas. No entanto, para que as transfusões sejam realizadas com segurança, é fundamental que o processo seja bem planejado. Agendar a sua doação é uma maneira de garantir que os hemocentros possam atender à demanda por sangue de forma eficiente e organizada, evitando filas e garantindo que haja sempre um estoque disponível para os momentos de necessidade.</p>

      <hr>
      <h2>Passo 1: Verifique se você está apto a doar</h2>
      <p>Antes de agendar, é importante garantir que você atende aos requisitos necessários para a doação. Veja abaixo os critérios de elegibilidade:</p>
      <li>Idade: O doador deve ter entre 16 e 69 anos. Menores de 18 anos precisam de autorização dos pais ou responsáveis.</li>
      <li>Peso: É necessário pesar mais de 50 kg.</li>
      <li>Saúde: O doador deve estar em boas condições de saúde, sem apresentar sintomas de doenças agudas como gripes ou resfriados.</li>
      <li>Documentação: Leve um documento oficial com foto (RG, CNH, etc.) no momento da doação.</li>
      <li>Descanso: O doador deve estar bem alimentado e descansado no dia da doação, evitando, por exemplo, doações após atividades físicas intensas ou noites mal dormidas.</li>
      <li>Interrupção de Tatuagens: Se você fez tatuagens ou piercings nos últimos 12 meses, deverá esperar esse período para doar.</li>

      <hr>
      <h2>Passo 2: Documentos necessários</h2>
      <p>Para realizar o seu agendamento e, posteriormente, a doação, é fundamental que você tenha em mãos um documento oficial com foto. Aceitamos os seguintes documentos:</p>
      <li>RG (Carteira de Identidade)</li>
      <li>CNH (Carteira Nacional de Habilitação)</li>
      <li>Passaporte (para doadores estrangeiros)</li>
      <p>Importante: Não será permitido realizar a doação caso você não tenha um documento válido com foto.</p>

      <hr>
      <h2>Passo 3: Escolha o local e horário para agendamento</h2>
      <p>Aqui no Laço Vermelho, oferecemos a possibilidade de escolher o local mais próximo de você e o melhor horário para a doação. Trabalhamos em parceria com vários hemocentros e hospitais da região, facilitando o acesso ao processo de doação. Veja como escolher:</p>
      <li>Locais de doação: Através da nossa plataforma, você pode consultar os pontos de doação disponíveis, com a indicação de seus horários de funcionamento. Escolha o local que mais se adequa à sua localização.</li>
      <li>Horários disponíveis: A doação de sangue é realizada em horários específicos, conforme a disponibilidade do hemocentro. Após selecionar o local, você verá a lista de horários disponíveis para agendamento. Caso o horário desejado não esteja disponível, você poderá escolher outro ou ser notificado caso alguma vaga se abra.</li>

      <hr>
      <h2>Passo 4: Realizando o agendamento</h2>
      <p>Agora que você tem todas as informações em mãos, pode realizar seu agendamento diretamente em nossa plataforma. O processo é simples e rápido:</p>
      <li>Preencha o formulário de agendamento: Forneça seus dados pessoais (nome completo, data de nascimento, CPF, telefone, e-mail, etc.).</li>
      <li>Selecione o local e horário: Escolha o hemocentro ou hospital onde deseja realizar a doação e selecione o horário disponível.</li>
      <li>Confirmar agendamento: Após preencher as informações, confira os dados e clique em Confirmar Agendamento.</li>
      <li>Receba a confirmação: Você receberá um e-mail e uma mensagem SMS confirmando o seu agendamento, com todos os detalhes, incluindo o endereço do local e o horário escolhido.</li>

      <hr>
      <h2>Passo 5: Prepare-se para a doação</h2>
      <p>Antes de comparecer ao local agendado, recomendamos algumas ações para garantir que a doação seja feita com segurança e conforto:</p>
      <li>Hidrate-se bem: Beba líquidos antes da doação, como água ou sucos naturais.</li>
      <li>Faça uma refeição leve: Evite alimentos gordurosos e pesados antes da doação. Prefira refeições leves e equilibradas, como frutas, arroz e feijão.</li>
      <li>Evite álcool e café: 24 horas antes da doação, evite ingerir bebidas alcoólicas e cafeína, pois podem interferir no processo de doação e na recuperação após a coleta.</li>
      <li>Evite esforços físicos: Não realize atividades físicas intensas nas 24 horas anteriores à doação.</li>
      <li>Use roupas confortáveis: Escolha roupas de manga curta ou com mangas que podem ser facilmente dobradas, pois a doação será realizada em um dos seus braços.</li>

      <hr>
      <h2>Passo 6: No dia da doação</h2>
      <p>Quando chegar ao local de doação, siga as instruções da equipe do hemocentro. Eles farão um pequeno questionário sobre sua saúde e hábitos, além de realizar uma triagem rápida para verificar se você está apto a doar naquele momento.</p>
      <p>Após a doação, é normal sentir-se um pouco cansado. Por isso, é importante:</p>
      <li>Descansar: Após a coleta, você será orientado a descansar por alguns minutos e a tomar um suco ou lanche para ajudar na recuperação.</li>
      <li>Não realizar esforços imediatos: Evite atividades físicas intensas nas primeiras horas após a doação.</li>

      <hr>
      <h2>Passo 7: Cuidados pós-doação</h2>
      <p>Após a doação, é importante que você siga algumas recomendações para garantir que a recuperação seja tranquila e rápida:</p>
      <li>Evite esforços físicos por 24 horas.</li>
      <li>Aumente a ingestão de líquidos para repor a quantidade de plasma doado.</li>
      <li>Coma alimentos leves nos próximos dias, garantindo que sua alimentação esteja balanceada e saudável.</li>
      <li>Não se esqueça de seguir a recomendação de doação regular, que é de, no mínimo, 3 meses entre as doações.</li>

      <hr>
      <h2>Cancelamento ou alteração do agendamento</h2>
      <p>Se, por algum motivo, você não puder comparecer no horário agendado, pedimos que nos avise com antecedência. Você pode cancelar ou reagendar sua doação diretamente na nossa plataforma de agendamento, com a facilidade de escolher um novo horário.</p>

      <hr>
      <h2 class="text-branco background">Dúvidas frequentes (FAQ)</h2>
      <h3>1. Posso doar sangue se estiver tomando medicamentos?</h3>
      <p>Depende do medicamento e da condição de saúde. Consulte um médico ou o hemocentro para verificar a possibilidade de doação.</p>
      <h3>2. Posso doar sangue se tiver acabado de tomar vacina?</h3>
      <p>Recomendamos esperar 30 dias após a vacinação para doar sangue, principalmente se for vacina contra doenças como a gripe.</p>
      <h3>3. Quanto tempo demora para a coleta de sangue?</h3>
      <p>A coleta de sangue dura, em média, de 10 a 15 minutos. O processo total, incluindo a triagem e o descanso pós-doação, pode durar cerca de 1 hora.</p>
